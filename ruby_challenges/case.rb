weather = 'sunny'

case weather
when 'cloudy'
  puts "It's couldy outside. Take a jacket."
when 'windy'
  puts "It's windy outside. Don't forget your cap."
when 'rainy'
  puts "It's rainy. Take your umbrella."
when 'foggy'
  puts "It's foggy today. You won't see much."
when 'snowing'
  puts "It's snowing. Let's build a snowman!"
when 'sunny'
  puts "It's sunny outside. Don't forget your sunglasses and let's go to the beach!"
else
  puts "Who knows. It's better to be prepared for anything."
end
